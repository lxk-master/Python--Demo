'''
定义一个函数,计算num01加上num02的结果并返回值
'''

def add(num01, num02):
    return num01 + num02

def sub(num01,num02):
    return num01 - num02

def mul(num01, num02):
    return num01 * num02

def div(num01, num02):
    return num01 / num02

if __name__ == "__main__":
    print(add(100,200))
    print(div(100,200))