'''
函数参数的使用
    在函数名的后面的小括号内填写参数
    多个参数之间使用 , 分隔

参数的作用
    函数把具有独立功能的代码块组织的一个小模块,在需要的时候调用
    函数的参数,增加函数的通用性,针对相同数据的处理逻辑,能够适应更多的数据
        1、在函数内部,把参数当作变量使用,进行需要的数据处理
        2、函数调用时,按照函数定义的参数顺序,把希望在函数内部处理的数据,通过参数传递

函数的返回值
    在程序开发中,有时候,会希望一个函数执行结束后,告诉调用者一个结果,以便调用者针对具体的结果作后续的处理
    返回值是函数完成工作后,最后给调用的一个结果
    在函数调用中使用 return 关键字可以返回结果
    调用函数一方,可以使用变量来接收函数的返回结果
    注意: return 表示返回,后续的代码都不会执行
'''
